-- ============================================================
--  neovide.lua — настройки графического клиента Neovide
--  Подключается из init.lua строкой: require("neovide")
-- ============================================================

-- Весь конфиг выполняется ТОЛЬКО если запущен Neovide.
-- В обычном терминале vim.g.neovide = nil, и блок просто пропускается.
if not vim.g.neovide then
  return
end

-- ------------------------------------------------------------
--  ШРИФТ
-- ------------------------------------------------------------
-- Шрифт и размер. Имя шрифта — как в системе. h14 = высота 14.
-- Если шрифта с лигатурами нет — поставь любой моноширинный.
-- vim.o.guifont = "JetBrainsMono Nerd Font:h14"

-- ------------------------------------------------------------
--  МАСШТАБ И РАЗМЕР ОКНА
-- ------------------------------------------------------------
-- Общий масштаб всего интерфейса (1.0 = 100%). Удобно крутить на лету.
vim.g.neovide_scale_factor = 1.0

-- Хоткеи для изменения масштаба на лету (Ctrl + = / Ctrl + -)
local function change_scale(delta)
  vim.g.neovide_scale_factor = vim.g.neovide_scale_factor + delta
end
vim.keymap.set("n", "<C-=>", function() change_scale(0.1) end, { desc = "Neovide: увеличить" })
vim.keymap.set("n", "<C-->", function() change_scale(-0.1) end, { desc = "Neovide: уменьшить" })

-- Отступы вокруг текста (в пикселях). Делает окно "воздушнее".
vim.g.neovide_padding_top    = 8
vim.g.neovide_padding_bottom = 8
vim.g.neovide_padding_left   = 8
vim.g.neovide_padding_right  = 8

-- ------------------------------------------------------------
--  ПРОЗРАЧНОСТЬ И ФОН
-- ------------------------------------------------------------
-- Прозрачность окна: 0.0 = полностью прозрачное, 1.0 = непрозрачное.
vim.g.neovide_opacity = 1
-- vim.g.neovide_normal_opacity = 1

-- Размытие фона под прозрачным окном (только macOS/Wayland, на Windows игнорируется).
vim.g.neovide_window_blurred = false

-- ------------------------------------------------------------
--  АНИМАЦИЯ КУРСОРА (то самое "размытое движение")
-- ------------------------------------------------------------
-- Длительность анимации перемещения курсора, в секундах.
-- Больше = курсор дольше "летит" до новой позиции (более плавно/размыто).
-- 0 = мгновенный прыжок без анимации.
vim.g.neovide_cursor_animation_length = 0.13

-- Длина "хвоста" за курсором (0.0–1.0+). Чем больше — тем сильнее смазывание.
vim.g.neovide_cursor_trail_size = 0.4

-- Отдельная (короткая) длительность для мелких движений, например h/j/k/l.
-- Чтобы при печати курсор не "тормозил".
vim.g.neovide_cursor_short_animation_length = 0.35

-- Анимировать ли курсор в режиме вставки (insert mode).
vim.g.neovide_cursor_animate_in_insert_mode = true

-- Анимировать ли курсор в командной строке (когда жмёшь :).
vim.g.neovide_cursor_animate_command_line = true

-- Плавное мигание курсора (затухание вместо резкого вкл/выкл).
vim.g.neovide_cursor_smooth_blink = true

-- ------------------------------------------------------------
--  VFX — эффекты частиц за курсором
-- ------------------------------------------------------------
-- Режим спецэффекта. Варианты:
--   "railgun"   — частицы закручиваются спиралью (классика)
--   "torpedo"   — плотный смазанный шлейф
--   "pixiedust" — рассыпающиеся частицы-пыль
--   "sonicboom" — ударная волна
--   "ripple"    — волны-круги
--   "wireframe" — каркасный след
--   ""          — выключить эффект (останется только trail)
vim.g.neovide_cursor_vfx_mode = ""

-- Прозрачность частиц (0–255). Меньше = бледнее.
vim.g.neovide_cursor_vfx_opacity = 200.0

-- Сколько секунд живёт каждая частица. Больше = длиннее шлейф.
vim.g.neovide_cursor_vfx_particle_lifetime = 1.2

-- Плотность частиц (сколько их генерируется). Больше = гуще эффект.
vim.g.neovide_cursor_vfx_particle_density = 25.0

-- Скорость разлёта частиц.
vim.g.neovide_cursor_vfx_particle_speed = 14.0

-- --- Параметры только для railgun: ---
-- Сдвиг фазы спирали (закрученность).
vim.g.neovide_cursor_vfx_particle_phase = 1.5
-- Степень закрутки частиц.
vim.g.neovide_cursor_vfx_particle_curl = 1.0

-- ------------------------------------------------------------
--  ПЛАВНАЯ ПРОКРУТКА
-- ------------------------------------------------------------
-- Длительность анимации скролла. 0 = выключить плавную прокрутку.
vim.g.neovide_scroll_animation_length = 0.2

-- На сколько строк за пределами экрана прерывать анимацию скролла
-- (чтобы long jump-ы не анимировались слишком долго).
vim.g.neovide_scroll_animation_far_lines = 1

-- ------------------------------------------------------------
--  ОКНО / ОС
-- ------------------------------------------------------------
-- Запоминать размер и позицию окна между запусками.
vim.g.neovide_remember_window_size = true

-- Полноэкранный режим при старте.
vim.g.neovide_fullscreen = false

-- Прятать указатель мыши во время печати.
vim.g.neovide_hide_mouse_when_typing = true

-- Подтверждать выход, если есть несохранённые изменения (:q без !).
vim.g.neovide_confirm_quit = true

-- Профиль производительности: false = выключить ограничитель FPS,
-- рисовать максимально плавно (жрёт чуть больше ресурсов).
vim.g.neovide_no_idle = true

-- Хоткей переключения полного экрана (F11)
vim.keymap.set("n", "<F11>", function()
  vim.g.neovide_fullscreen = not vim.g.neovide_fullscreen
end, { desc = "Neovide: полный экран" })

-- 200 hz
vim.g.neovide_refresh_rate = 185

-- FPS, когда окно НЕ в фокусе (свернуто/другое окно сверху).
-- Низкое значение экономит ресурсы, на плавность не влияет.
vim.g.neovide_refresh_rate_idle = 5

